from time import sleep

def grabProfilePicture(browser, um):
    username = um + "\'s profile picture"
    USER_XPATH ='//img[@alt="%s"]' % username
    img = browser.find_element_by_xpath(USER_XPATH)
    src = img.get_attribute('src')
    return src
